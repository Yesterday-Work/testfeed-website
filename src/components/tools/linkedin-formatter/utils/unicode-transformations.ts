export function transformText(text: string, transformMap: Record<string, string>): string {
  let result = '';
  
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    result += transformMap[char] || char;
  }
  
  return result;
}

export function transformSelectedText(
  textarea: HTMLTextAreaElement, 
  transformMap: Record<string, string>
): void {
  if (!textarea) return;
  
  const start = textarea.selectionStart;
  const end = textarea.selectionEnd;
  
  if (start === end) return;
  
  const selection = textarea.value.substring(start, end);
  const transformed = transformText(selection, transformMap);
  
  const newText = 
    textarea.value.substring(0, start) + 
    transformed + 
    textarea.value.substring(end);
  
  textarea.value = newText;
  
  textarea.selectionStart = start;
  textarea.selectionEnd = start + transformed.length;
  
  textarea.dispatchEvent(new Event('input'));
  
  textarea.focus();
} 
